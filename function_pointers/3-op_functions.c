#include <stdio.h>

/**
 * op_add - sum operator
 * @a: one parametr
 * @b: two
 *
 * Return: a+b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtraction
 * @a: sub_onw
 * @b: sub_two
 *
 * Return: a-b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - subtraction
 * @a: mul_onw
 * @b: mul_two
 *
 * Return: a*b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - subtraction
 * @a: div_onw
 * @b: div_two
 *
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - subtraction
 * @a: mod_onw
 * @b: mod_two
 *
 * Return: a / b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
